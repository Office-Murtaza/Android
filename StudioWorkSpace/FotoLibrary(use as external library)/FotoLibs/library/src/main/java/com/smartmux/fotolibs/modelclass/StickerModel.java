package com.smartmux.fotolibs.modelclass;

public class StickerModel {
	private  int number;
	private int sticker;
	
	public StickerModel(int number, int sticker) {
		super();
		this.number = number;
		this.sticker = sticker;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public int getSticker() {
		return sticker;
	}
	public void setSticker(int sticker) {
		this.sticker = sticker;
	}
	
	

}
