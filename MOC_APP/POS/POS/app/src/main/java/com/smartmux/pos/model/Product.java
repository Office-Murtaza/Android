public class Product {

    String productID;
    String productPrice;
    String productName;
    String productManufacture;
    String productThumb;
    String productCategoryID;
    String productExPareDate;
    String productMinStockQuantity;
    String productUnit;
    String productSupplierID;
    String productQuantity;


    public String getProductID() {
        return productID;
    }

    public void setProductID(String productID) {
        this.productID = productID;
    }

    public String getProductPrice() {
        return productPrice;
    }

    public void setProductPrice(String productPrice) {
        this.productPrice = productPrice;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getProductManufacture() {
        return productManufacture;
    }

    public void setProductManufacture(String productManufacture) {
        this.productManufacture = productManufacture;
    }

    public String getProductThumb() {
        return productThumb;
    }

    public void setProductThumb(String productThumb) {
        this.productThumb = productThumb;
    }

    public String getProductCategoryID() {
        return productCategoryID;
    }

    public void setProductCategoryID(String productCategoryID) {
        this.productCategoryID = productCategoryID;
    }

    public String getProductExPareDate() {
        return productExPareDate;
    }

    public void setProductExPareDate(String productExPareDate) {
        this.productExPareDate = productExPareDate;
    }

    public String getProductMinStockQuantity() {
        return productMinStockQuantity;
    }

    public void setProductMinStockQuantity(String productMinStockQuantity) {
        this.productMinStockQuantity = productMinStockQuantity;
    }

    public String getProductUnit() {
        return productUnit;
    }

    public void setProductUnit(String productUnit) {
        this.productUnit = productUnit;
    }

    public String getProductSupplierID() {
        return productSupplierID;
    }

    public void setProductSupplierID(String productSupplierID) {
        this.productSupplierID = productSupplierID;
    }

    public String getProductQuantity() {
        return productQuantity;
    }

    public void setProductQuantity(String productQuantity) {
        this.productQuantity = productQuantity;
    }
}
